package jhaturanga.model.editor;

public interface StringBoard {
    /**
     * Use this method to get the board.
     * 
     * @return String - The board as a String.
     */
    String getBoard();

    /**
     * Use this method to get the number of rows of the board.
     * 
     * @return int - the number of rows of the StringBoard.
     */
    int getRows();

    /**
     * Use this method to get the number of columns of the board.
     * 
     * @return int - the number of columns of the StringBoard.
     */
    int getColumns();
}
